package entity;

import java.util.Date;

public class Votante {
	
	private Integer id_votante;
	private String ci;
	private String nombre;
	private String apellido;
	private String sufrago;
	private String habilitado;
	private String id_mesa;
	private String id_evento;
	private String paisActual;
	
	public Integer getId_votante() {
		return id_votante;
	}
	public void setId_votante(Integer id_votante) {
		this.id_votante = id_votante;
	}
	public String getCi() {
		return ci;
	}
	public void setCi(String ci) {
		this.ci = ci;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public String getSufrago() {
		return sufrago;
	}
	public void setSufrago(String sufrago) {
		this.sufrago = sufrago;
	}
	public String getHabilitado() {
		return habilitado;
	}
	public void setHabilitado(String habilitado) {
		this.habilitado = habilitado;
	}
	public String getId_mesa() {
		return id_mesa;
	}
	public void setId_mesa(String id_mesa) {
		this.id_mesa = id_mesa;
	}
	public String getId_evento() {
		return id_evento;
	}
	public void setId_evento(String id_evento) {
		this.id_evento = id_evento;
	}
	public String getPaisActual() {
		return paisActual;
	}
	public void setPaisActual(String paisActual) {
		this.paisActual = paisActual;
	}
	
	
	
	
	


}
